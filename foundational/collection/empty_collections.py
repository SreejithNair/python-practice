#To create an empty collection of T where type will be inferred 

empty_a = () #empty tuple
print(type(empty_a))

empty_b = {} #empty dictionary
print(type(empty_b))

empty_c = [] #empty list
print(type(empty_c))

empty_d = set() #empty set
print(type(empty_d))

empty_e = range(0) #empty range
print(type(empty_e))

empty_f = frozenset() #empty frozenset
print(type(empty_f))